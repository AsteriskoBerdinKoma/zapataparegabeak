package gkae.zapataparegabeak.objektuak;

public class Albarana {
	private String id;
	
	public Albarana(String id){
		super();
		this.id = id;
		
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}
	
	public String toString(){
		return this.id;
	}
	

}
